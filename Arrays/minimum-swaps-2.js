/**
 * 
 * arr       : [7, 1, 3, 2, 4, 5, 6]
 * swaps     : 0
 * condition : arr[i] !== i+1

|----+---------------------------+----------------+----------------+------------------------|
| i  | updated array             | current number | correct number | arr[i] !== i+1 | swaps | 
|----+---------------------------+----------------+----------------+----------------|-------|
| 0  |  (7), (1), 3, 2, 4, 5, 6  | 7              | 0 + 1 = 1      | true           | 1     |
|----+---------------------------+----------------+----------------+----------------|-------|
| 1  |  1, (7), 3, (2), 4, 5, 6  | 7              | 1 + 1 = 2      | true           | 2     |
|----+---------------------------+----------------+----------------+----------------|-------|
| 2  |  1, 2, (3), 7, 4, 5, 6    | 3              | 2 + 1 = 3      | false          | 2     |
|----+---------------------------+----------------+----------------+----------------|-------|
| 3  |  1, 2, 3, (7), (4), 5, 6  | 7              | 3 + 1 = 4      | true           | 3     |
|----+---------------------------+----------------+----------------+----------------|-------|
| 4  |  1, 2, 3, 4, (7), (5), 6  | 7              | 4 + 1 = 5      | true           | 4     |
|----+---------------------------+----------------+----------------+----------------|-------|
| 5  |  1, 2, 3, 4, 5, (7), (6)  | 7              | 5 + 1 = 6      | true           | 5     |
|----+---------------------------+----------------+----------------+----------------|-------|
| 6  |  1, 2, 3, 4, 5, 6, (7)    | 7              | 6 + 1 = 7      | false          | 5     |
|----+---------------------------+----------------+----------------+----------------|-------|

*/

function minimumSwaps(arr) {
  const numbers = [...arr];
  let swaps = 0;

  for (let i = 0; i < numbers.length; i++) {
    const currentNumber = numbers[i];
    const correctNumber = i + 1;

    if (currentNumber !== correctNumber) {
      const correctNumberIndex = numbers.indexOf(correctNumber, i);

      numbers[i] = numbers[correctNumberIndex];
      numbers[correctNumberIndex] = currentNumber;

      swaps++;
    }
  }

  return swaps;
}

console.log(minimumSwaps([7, 1, 3, 2, 4, 5, 6])); // 5
